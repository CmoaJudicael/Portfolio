<h1 class="m-5">Qu'est-ce qu'un réseau ?</h1>

<p>Un ensemble d'entités (objets, personnes, machines, etc.) interconnectées les unes avec les autres <br>
Exemples :  
</p>
<ul>
    <li>réseau de transport</li>
    <li>réseau téléphonique</li>
    <li>réseau de neurones</li>
</ul>

<h1>Réseaux informatique</h1>

<p>
Un ensemble d'ordinateurs reliés entre eux grâce à des lignes physiques et
échangeant des informations sous forme de données numériques
</p>
