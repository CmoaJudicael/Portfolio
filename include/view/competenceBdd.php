<h1 class="m-5">Une base de données est un ensemble d’informations structurées mémorisées sur un support persistant.</h1>

<p>

Une base de données est un ensemble (potentiellement volumineux, mais pas forcément) d'informations conformes à une structure pré-définie au moment de la conception, avec de plus, une caractéristique essentielle : 
</p>
<br>  
<ul>
    <li>On souhaite les mémoriser de manière persistante. La persistance désigne la capacité d’une base à exister indépendamment des applications qui la manipulent, ou du système qui l’héberge. 
    </li>
    <li>On peut arrêter toutes les machines un soir, et retrouver la base de données le lendemain. Cela implique qu’une base est toujours stockée sur un support comme les disques magnétiques qui préservent leur contenu même en l’absence d’alimentation électrique.
    </li>
</ul>
</p>
