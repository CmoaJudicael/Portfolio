    <h1 class="Color-lightBlue">REGLE</h1>
    <div class='content_article d-flex'>
      <section class="d-flex justify-content-around">
        <div>
          <p>Le Jeu de la vie est un « jeu à zéro joueur », puisqu'il ne nécessite aucune intervention du joueur lors de son déroulement. Il s’agit d’un automate cellulaire, un modèle où chaque état conduit mécaniquement à l’état suivant à partir de règles préétablies.

Le jeu se déroule sur une grille à deux dimensions, théoriquement infinie, dont les cases — appelées « cellules », par analogie avec les cellules vivantes — peuvent prendre deux états distincts : « vivante » ou « morte ».

Une cellule possède huit voisins, qui sont les cellules adjacentes horizontalement, verticalement et diagonalement.

À chaque itération, l'état d’une cellule est entièrement déterminé par l’état de ses huit cellules voisines, selon les règles suivantes :</p>
          <br>
          <ul>
            <li>une cellule morte possédant exactement trois cellules voisines vivantes devient vivante (elle naît).</li><br>
            <li>une cellule vivante possédant deux ou trois cellules voisines vivantes le reste, sinon elle meurt.</li><br>
          </ul>
          <p>L'état suivant d'une cellule est : (S = 3) OU (E = 1 ET S = 2). <br>Avec :</p>
          <ul>
              <li>S : nombre actuel de cellules vivantes dans son voisinage (entier naturel compris entre 0 et 8 inclus).</li>
              <li>E : état actuel de la cellule (entier naturel égal à 0 pour une cellule morte et égal à 1 pour une cellule vivante).</li>
          </ul>
        </div>
