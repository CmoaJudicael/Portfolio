        <h1>PHP</h1>
        <div class="d-flex justify-content-end align-items-center">
            
        <img class="logoCode m-2 p-2" src="include/img/php.png" alt="Logo PHP">
        <p class="m-2 txtLangage">Le PHP, pour Hypertext Preprocessor, désigne un langage informatique, ou un langage de script, utilisé principalement pour la conception de sites web dynamiques. Il s'agit d'un langage de programmation sous licence libre qui peut donc être utilisé par n'importe qui de façon totalement gratuite.

Créé au début des années 1990 par le Canadien et Groenlandais Rasmus Lerdorf, le langage PHP est souvent associé au serveur de base de données MySQL et au serveur Apache. Avec le système d'exploitation Linux, il fait partie intégrante de la suite de logiciels libres LAMP.</p>

