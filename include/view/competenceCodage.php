        <h1>PYTHON</h1>
        <div class="d-flex justify-content-end align-items-center">
            
        <img class="logoCode m-2 p-2" src="include/img/python.webp" alt="Logo python">
        <p class="m-2 txtLangage">Python est le langage de programmation open source le plus employé par les informaticiens. Ce langage s’est propulsé en tête de la gestion d’infrastructure, d’analyse de données ou dans le domaine du développement de logiciels. En effet, parmi ses qualités, Python permet notamment aux développeurs de se concentrer sur ce qu’ils font plutôt que sur la manière dont ils le font. Il a libéré les développeurs des contraintes de formes qui occupaient leur temps avec les langages plus anciens. Ainsi, développer du code avec Python est plus rapide qu’avec d’autres langages.</p>

