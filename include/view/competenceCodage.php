        <div class="d-flex justify-content-end align-items-center">
            
        <img class="logoCode m-2 p-2" src="include/img/css-3.png" alt="Logo CSS">
        <p class="m-2 txtLangage">Le CSS pour Cascading Style Sheets, est un langage informatique utilisé sur Internet pour la mise en forme de fichiers et de pages HTML. On le traduit en français par feuilles de style en cascade.

Apparu dans les années 1990, le CSS se présente comme une alternative à la mise en forme via des balises, notamment HTML.</p>

