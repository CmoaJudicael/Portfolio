        <h1>JAVA</h1>
        <div class="d-flex justify-content-end align-items-center">
            
        <img class="logoCode m-2 p-2" src="include/img/java.webp" alt="Logo Java">

<p class="m-2 txtLangage">La technologie Java définit à la fois un langage de programmation orienté objet et une plateforme informatique. Créée par l'entreprise Sun Microsystems (souvent juste appelée "Sun") en 1995, et reprise depuis par la société Oracle en 2009, la technologie Java est indissociable du domaine de l'informatique et du Web. On la retrouve donc sur les ordinateurs, mais aussi sur les téléphones mobiles, les consoles de jeux, etc. L'avènement du smartphone et la puissance croissante des ordinateurs, ont entraîné un regain d'intérêt pour ce langage de programmation.</p>

