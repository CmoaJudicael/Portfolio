        <h1>HTML</h1>
        <div class="d-flex justify-content-end align-items-center">
            
        <img class="logoCode m-2 p-2" src="include/img/HTML5.png" alt="Logo html">
        <p class="m-2 txtLangage">L'HyperText Markup Language, HTML, désigne un type de langage informatique descriptif. Il s'agit plus précisément d'un format de données utilisé dans l'univers d'Internet pour la mise en forme des pages Web. Il permet, entre autres, d'écrire de l'hypertexte, mais aussi d'introduire des ressources multimédias dans un contenu.

Développé par le W3C (World Wide Web Consortium) et le WHATWG (Web Hypertext Application Technology Working Group), le format ou langage HTML est apparu dans les années 1990. Il a progressivement subi des modifications et propose depuis 2014 une version HTML5 plus aboutie.</p>

        </div>
    </article>
</div>
