    <h1 class="m-5">Qu’est-ce que le RGPD ?</h1>
    <img class="align-self-center" src="include/img/rgpd.jpg" alt="Image rgpd avec carte europe à l'arrière" style="width: 60%;">
    <p class="m-3 p-3">Le règlement général sur la protection des données (également connu sous le nom de RGPD) est une législation de l’Union européenne qui vise à s’assurer que les données personnelles soient traitées par les entreprises dans le cadre d’une base légale tout en assurant leur sécurité et leur confidentialité.

    Le RGPD concerne le traitement des données personnelles.

    Plusieurs cookies traitent les données personnelles des cookies. Le traitement de ces informations est sujet au RGPD.

    Pour traiter les données personnelles, vous avez besoin d’une base légale, c’est-à-dire une base juridique pour le traitement des données. Il peut s’agir d’un intérêt légitime, mais bien souvent, le traitement est basé sur le consentement.
    </p>
